//7种方块元素
export const blockMod = () => {
  let a = {
    site: [[0, 4], [0, 5], [0, 6], [0, 7]],
    color: color[0],
  };
  let b = {
    site: [[0, 4], [0, 5], [1, 4], [1, 5]],
    color: color[1],
  };
  let c = {
    site: [[0, 5], [1, 5], [2, 4], [2, 5]],
    color: color[2],
  };
  let d = {
    site: [[0, 4], [1, 4], [2, 4], [2, 5]],
    color: color[3],
  };
  let e = {
    site: [[0, 5], [1, 4], [1, 5], [1, 6]],
    color: color[4],
  };
  let f = {
    site: [[0, 5], [0, 6], [1, 4], [1, 5]],
    color: color[5],
  };
  let g = {
    site: [[0, 4], [0, 5], [1, 5], [1, 6]],
    color: color[6],
  };
  return ([a, b, c, d, e, f, g]);
};

